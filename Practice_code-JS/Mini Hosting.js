

// Normal function declaration.
function addOne(num){
    return num+1;
}

// Function with Expressions
let addTwo = function(num){
    return num+1;
}

console.log(addOne(5));
console.log(addTwo(5));

// Note :- Normal function can be call anywhere in your code, even before its actual declaration in the code.
// The function declaration is hoisted to the top of its containing scope during the compilation phase.

